"""Typst table exporter — renders report tables from a DuckDB run.

Produces four ``.typ`` include files consumed by
``docs/report/chapters/evaluation.typ``:

  - ``per-story.typ``         — one figure per story × all platforms
  - ``capability-tiers.typ``  — per-platform per-stage completion matrix
  - ``cross-cutting.typ``     — 4 Likert dimensions + overall per platform
  - ``resource.typ``          — tokens, cost, wall-clock, peak memory per platform

Reads from ``ResultStore`` for a specific ``run_id`` (defaults to the latest).
Stage-level completion for the capability-tiers table comes from the
``{execution_id}_stages.json`` sidecar files the runner writes to
``results/logs/<platform>/<story>/``.
"""

from __future__ import annotations

import json
from pathlib import Path
from typing import Any

import pandas as pd

from desmet.harness.store import ResultStore

STAGE_KEYS = ("requirements", "codegen", "testing", "deploy")
STAGE_LABELS = {
    "requirements": "Requirements",
    "codegen": "Code Gen",
    "testing": "Testing",
    "deploy": "Deploy",
}
# Canonical story ordering used in the report's per-story subsections.
DEFAULT_STORY_ORDER = ("US-001", "US-010", "US-030", "US-020")


def _escape(s: str) -> str:
    """Escape Typst special characters that appear in cell content."""
    return (
        str(s)
        .replace("\\", "\\\\")
        .replace("[", "\\[")
        .replace("]", "\\]")
        .replace("#", "\\#")
        .replace("$", "\\$")
    )


def _fmt_cost(val: Any) -> str:
    if val is None or pd.isna(val) or float(val) == 0:
        return "—"
    return f"${float(val):.3f}"


def _fmt_int(val: Any) -> str:
    if val is None or pd.isna(val):
        return "—"
    return f"{int(val):,}"


def _fmt_time(seconds: Any) -> str:
    if seconds is None or pd.isna(seconds) or float(seconds) <= 0:
        return "—"
    s = float(seconds)
    if s < 90:
        return f"{s:.1f}s"
    return f"{s / 60:.1f}m"


def _fmt_score(val: Any) -> str:
    if val is None or pd.isna(val):
        return "—"
    return f"{float(val):.2f}"


def _platform_display(pid: str) -> str:
    try:
        from desmet.infra import PLATFORM_NAMES
    except Exception:
        return pid
    return PLATFORM_NAMES.get(pid, pid)


def _stage_success_from_traces(
    platform_id: str,
    story_id: str,
    logs_dir: Path,
) -> dict[str, bool] | None:
    """Return ``{stage_name: success}`` from the most recent stages JSON."""
    trace_dir = logs_dir / platform_id / story_id
    if not trace_dir.exists():
        return None
    files = sorted(trace_dir.glob("*_stages.json"))
    if not files:
        return None
    try:
        data = json.loads(files[-1].read_text(encoding="utf-8"))
    except Exception:
        return None
    stages = data.get("stages", {})
    return {key: bool(entry.get("success")) for key, entry in stages.items()}


def _ordered_stories(story_ids: list[str]) -> list[str]:
    """Sort known stories first (canonical order), then unknowns alphabetically."""
    seen = set(story_ids)
    ordered = [s for s in DEFAULT_STORY_ORDER if s in seen]
    ordered += sorted(s for s in story_ids if s not in DEFAULT_STORY_ORDER)
    return ordered


def _ordered_platforms(platform_ids: list[str]) -> list[str]:
    """Sort platforms by display name for stable table ordering."""
    return sorted(platform_ids, key=lambda p: _platform_display(p).lower())


def _fmt_memory_mb(val: Any) -> str:
    """Format bytes as whole-number MB, ``—`` when missing or zero."""
    if val is None or pd.isna(val) or float(val) <= 0:
        return "—"
    return f"{int(float(val)) / (1024 * 1024):.0f} MB"


def _write_story_figure(sub: pd.DataFrame, story_id: str, out_path: Path) -> None:
    """Write a single figure for one story × all platforms."""
    platforms = _ordered_platforms([str(p) for p in sub["platform_id"].unique()])
    lines = [
        "// Auto-generated by `desmet export-typst`. Do not hand-edit.",
        "",
        '#import "../template.typ": completed-cell, failed-cell, score-cell',
        "",
        "#figure(",
        "  placement: none,",
        "  table(",
        "    columns: 7,",
        "    stroke: 0.5pt,",
        "    inset: 6pt,",
        "    align: left,",
        "    table.header(",
        "      [*Platform*], [*Status*], [*Wall-clock*], "
        "[*Tokens*], [*Cost*], [*Peak Memory*], [*Overall*],",
        "    ),",
    ]
    for pid in platforms:
        row = sub[sub["platform_id"] == pid].iloc[0]
        status_raw = str(row["status"])
        if status_raw == "completed":
            status_cell = "completed-cell"
        elif status_raw in ("failed", "error", "aborted"):
            status_cell = "failed-cell"
        else:
            status_cell = f"[{_escape(status_raw.replace('_', ' ').title())}]"
        tokens = int((row["tokens_input"] or 0) + (row["tokens_output"] or 0))
        overall = row.get("overall_score")
        overall_cell = (
            f'score-cell("{_fmt_score(overall)}")' if overall is not None and not pd.isna(overall)
            else "[—]"
        )
        cells = [
            f"[{_escape(_platform_display(pid))}]",
            status_cell,
            f"[{_escape(_fmt_time(row['wall_clock_seconds']))}]",
            f"[{_escape(_fmt_int(tokens))}]",
            f"[{_escape(_fmt_cost(row['cost_usd']))}]",
            f"[{_escape(_fmt_memory_mb(row.get('resource_peak_memory_bytes')))}]",
            overall_cell,
        ]
        lines.append("    " + ", ".join(cells) + ",")
    anchor = story_id.lower().replace("-", "")
    lines += [
        "  ),",
        f"  caption: [{_escape(story_id)} per-platform execution results],",
        f") <table-per-story-{anchor}>",
        "",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")


def export_per_story(exec_df: pd.DataFrame, out_dir: Path) -> dict[str, Path]:
    """Write one ``per-story-<sid>.typ`` file per story.

    Returns ``{story_id: output_path}`` so callers can see what was emitted.
    """
    story_ids = list(dict.fromkeys(exec_df["story_id"].tolist()))
    written: dict[str, Path] = {}
    for sid in _ordered_stories(story_ids):
        sub = exec_df[exec_df["story_id"] == sid]
        if sub.empty:
            continue
        path = out_dir / f"per-story-{sid}.typ"
        _write_story_figure(sub, sid, path)
        written[sid] = path
    return written


def export_capability_tiers(
    exec_df: pd.DataFrame,
    logs_dir: Path,
    out_path: Path,
) -> None:
    """Per-platform per-stage completion matrix.

    Tier per (platform, stage):
      - **Supported** — every story ran this stage successfully
      - **Partial** — at least one but not all
      - **Not Supported** — zero stories completed this stage
    """
    platforms = _ordered_platforms([str(p) for p in exec_df["platform_id"].unique()])
    matrix: dict[str, dict[str, tuple[int, int]]] = {}
    for pid in platforms:
        sub = exec_df[exec_df["platform_id"] == pid]
        counts: dict[str, tuple[int, int]] = {k: (0, 0) for k in STAGE_KEYS}
        for _, row in sub.iterrows():
            stages = _stage_success_from_traces(
                str(row["platform_id"]),
                str(row["story_id"]),
                logs_dir,
            )
            if not stages:
                continue
            for key in STAGE_KEYS:
                if key not in stages:
                    continue
                succ, total = counts[key]
                counts[key] = (succ + (1 if stages[key] else 0), total + 1)
        matrix[pid] = counts

    def tier(succ: int, total: int) -> str:
        if total == 0:
            return "—"
        if succ == total:
            return "Supported"
        if succ == 0:
            return "Not Supported"
        return "Partial"

    tier_cell = {
        "Supported": "supported-cell",
        "Not Supported": "not-supported-cell",
        "Partial": "partial-cell",
    }
    lines = [
        "// Auto-generated by `desmet export-typst`. Do not hand-edit.",
        "",
        '#import "../template.typ": supported-cell, not-supported-cell, partial-cell',
        "",
        "#figure(",
        "  placement: none,",
        "  table(",
        "    columns: 5,",
        "    stroke: 0.5pt,",
        "    inset: 6pt,",
        "    align: left,",
        "    table.header(",
        "      [*Platform*], [*Requirements*], [*Code Gen*], [*Testing*], [*Deploy*],",
        "    ),",
    ]
    for pid in platforms:
        cells = [f"[{_escape(_platform_display(pid))}]"]
        for key in STAGE_KEYS:
            t = tier(*matrix[pid][key])
            cells.append(tier_cell.get(t, f"[{_escape(t)}]"))
        lines.append("    " + ", ".join(cells) + ",")
    lines += [
        "  ),",
        "  caption: [Pipeline capability tiers per stage],",
        ") <table-capability-tiers>",
        "",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")


def export_cross_cutting(exec_df: pd.DataFrame, out_path: Path) -> None:
    """4 Likert dimensions + overall, averaged per platform."""
    platforms = _ordered_platforms([str(p) for p in exec_df["platform_id"].unique()])
    lines = [
        "// Auto-generated by `desmet export-typst`. Do not hand-edit.",
        "",
        '#import "../template.typ": score-cell',
        "",
        "#figure(",
        "  placement: none,",
        "  table(",
        "    columns: 6,",
        "    stroke: 0.5pt,",
        "    inset: 6pt,",
        "    align: left,",
        "    table.header(",
        "      [*Platform*], [*Pipeline Compl.*], [*Efficiency*], "
        "[*Orchestration*], [*Autonomy*], [*Overall*],",
        "    ),",
    ]
    for pid in platforms:
        sub = exec_df[exec_df["platform_id"] == pid]
        cells = [f"[{_escape(_platform_display(pid))}]"]
        for col in (
            "score_pipeline_completeness",
            "score_efficiency",
            "score_orchestration",
            "score_autonomy",
            "overall_score",
        ):
            val = sub[col].mean() if col in sub else None
            if val is None or pd.isna(val):
                cells.append("[—]")
            else:
                cells.append(f'score-cell("{_fmt_score(val)}")')
        lines.append("    " + ", ".join(cells) + ",")
    lines += [
        "  ),",
        "  caption: [Cross-cutting framework dimension scores (1--5 Likert)],",
        ") <table-cross-cutting>",
        "",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")


def export_resource(exec_df: pd.DataFrame, out_path: Path) -> None:
    """Resource consumption summed across evaluated stories per platform."""
    platforms = _ordered_platforms([str(p) for p in exec_df["platform_id"].unique()])
    lines = [
        "// Auto-generated by `desmet export-typst`. Do not hand-edit.",
        "",
        "#figure(",
        "  placement: none,",
        "  table(",
        "    columns: 6,",
        "    stroke: 0.5pt,",
        "    inset: 6pt,",
        "    align: left,",
        "    table.header(",
        "      [*Platform*], [*Stories*], [*Tokens*], [*Cost*], "
        "[*Wall-clock*], [*Peak Memory*],",
        "    ),",
    ]
    for pid in platforms:
        sub = exec_df[exec_df["platform_id"] == pid]
        stories = len(sub)
        tokens = int(
            (sub["tokens_input"].fillna(0) + sub["tokens_output"].fillna(0)).sum()
        )
        cost = float(sub["cost_usd"].fillna(0).sum())
        wall = float(sub["wall_clock_seconds"].fillna(0).sum())
        memory = sub["resource_peak_memory_bytes"].dropna()
        memory_s = (
            f"{int(memory.max()) / (1024 * 1024):.0f} MB"
            if len(memory) > 0
            else "—"
        )
        cells = [
            _escape(_platform_display(pid)),
            str(stories),
            _escape(_fmt_int(tokens)),
            _escape(_fmt_cost(cost)),
            _escape(_fmt_time(wall)),
            _escape(memory_s),
        ]
        lines.append("    " + ", ".join(f"[{c}]" for c in cells) + ",")
    lines += [
        "  ),",
        "  caption: [Resource consumption per platform across evaluated stories],",
        ") <table-resource>",
        "",
    ]
    out_path.write_text("\n".join(lines), encoding="utf-8")


def export_all(
    run_id: str | None = None,
    out_dir: Path | None = None,
    db_path: Path | None = None,
    logs_dir: Path | None = None,
) -> dict[str, Path]:
    """Generate all Typst tables for a single run.

    Defaults: latest run, writes to ``docs/report/generated/``, reads
    DuckDB from ``results/desmet.duckdb`` and stage traces from
    ``results/logs/``.  Returns ``{table_name: output_path}``.
    """
    root = Path(__file__).resolve().parents[3]
    db_path = Path(db_path) if db_path else (root / "results" / "desmet.duckdb")
    logs_dir = Path(logs_dir) if logs_dir else (root / "results" / "logs")
    out_dir = Path(out_dir) if out_dir else (root / "docs" / "report" / "generated")
    out_dir.mkdir(parents=True, exist_ok=True)

    store = ResultStore(db_path=db_path)
    target = run_id or store.latest_run_id()
    if not target:
        raise ValueError("No run_id available (database empty)")

    exec_df = store.get_executions(target)
    if exec_df.empty:
        raise ValueError(f"No executions found for run {target}")

    outputs: dict[str, Path] = {}

    story_outputs = export_per_story(exec_df, out_dir)
    for sid, path in story_outputs.items():
        outputs[f"per_story_{sid}"] = path

    for name, fn in (
        ("capability_tiers", lambda p: export_capability_tiers(exec_df, logs_dir, p)),
        ("cross_cutting", lambda p: export_cross_cutting(exec_df, p)),
        ("resource", lambda p: export_resource(exec_df, p)),
    ):
        path = out_dir / f"{name.replace('_', '-')}.typ"
        fn(path)
        outputs[name] = path
    return outputs
